//while and do-while loop

let index=0;

while(index<=10){
    console.log(`Value of index is: ${index}`);
    index=index+2;
}

let myArr=["flash","batman","superman"];

let arr=0;

while(arr<myArr.length){
    console.log(`Value is ${myArr[arr]}`);
    arr=arr+1;
}

let score=1;

do{
    console.log(score);
    score++;
}while(score<=10);
